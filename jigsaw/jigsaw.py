from jigsaw.jigsawSudoku import JigsawSudoku

def createSudoku():
    shape = []
    shape.append( [0,0,0,1,1,1,2,2,2] )
    shape.append( [0,3,0,1,1,1,2,4,2] )
    shape.append( [0,3,0,1,5,1,2,4,2] )
    shape.append( [0,3,0,1,5,5,5,4,2] )
    shape.append( [6,3,3,3,5,4,4,4,2] )
    shape.append( [6,3,5,5,5,7,8,4,8] )
    shape.append( [6,3,6,7,5,7,8,4,8] )
    shape.append( [6,3,6,7,7,7,8,4,8] )
    shape.append( [6,6,6,7,7,7,8,8,8] )
    sudoku = JigsawSudoku( 9, shape)
    sudoku.Set( 0, 0, 1, True)
    sudoku.Set( 0, 3, 4, True)
    sudoku.Set( 0, 8, 9, True)
    sudoku.Set( 1, 5, 2, True)
    sudoku.Set( 1, 8, 4, True)
    sudoku.Set( 3, 3, 9, True)
    sudoku.Set( 3, 4, 8, True)
    sudoku.Set( 3, 8, 7, True)
    sudoku.Set( 4, 1, 8, True)
    sudoku.Set( 4, 2, 7, True)
    sudoku.Set( 4, 4, 6, True)
    sudoku.Set( 4, 6, 2, True)
    sudoku.Set( 4, 7, 5, True)
    sudoku.Set( 5, 0, 6, True)
    sudoku.Set( 5, 4, 9, True)
    sudoku.Set( 5, 5, 8, True)
    sudoku.Set( 7, 0, 3, True)
    sudoku.Set( 7, 3, 1, True)
    sudoku.Set( 8, 0, 8, True)
    sudoku.Set( 8, 5, 9, True)
    sudoku.Set( 8, 8, 5, True)
    sudoku.DoChange() # set the changes
    return sudoku
